package döngüler;

public class Dortislem {
	
	
	
	public int Gather(int... sayilar) {
		int total=0;
		for(int sayi:sayilar) {
			total+=sayi;
			
			
				
		}
		return total;
		
		
	}
	public int Extraction(int... sayilar) {
		int total=0;
		for(int sayi:sayilar) {
			total-=sayi;
			
			
				
		}
		return total;
		
		
	}
	public int Divide(int... sayilar) {
		int total=0;
		for(int sayi:sayilar) {
			total/=sayi;
			
			
				
		}
		return total;
		
		
	}
	public int Impact(int... sayilar) {
		int total=0;
		for(int sayi:sayilar) {
			total*=sayi;
			
			
				
		}
		return total;
		
		
	}

}
