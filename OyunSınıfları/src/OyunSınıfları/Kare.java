package OyunSınıfları;

public class Kare {
	private double x;
	private double y;
	private double uzunluk;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getUzunluk() {
		return uzunluk;
	}
	public void setUzunluk(double uzunluk) {
		this.uzunluk = uzunluk;
	}
}
